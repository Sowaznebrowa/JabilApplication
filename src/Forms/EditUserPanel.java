    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        String query = "SELECT * FROM "+PropertiesClass.props.DB_NAME+".User WHERE Login='"+jTextField1.getText()+"';";
        boolean check = sqlConnector.checkQueryAppearence(query);
        if((!check || jTextField1.getText().contentEquals(selectedUser)) && !(jTextField1.getText().isEmpty())){
        query = "SELECT ID FROM "+PropertiesClass.props.DB_NAME+".Group WHERE DataName='"+jList1.getSelectedValue().toString()+"';";
        int groupID = Integer.parseInt(sqlConnector.getQueryFirstElement(query));
        
        int carrierID=-1;   
        if(jComboBox2.getSelectedIndex()!=-1){
           query = "SELECT ID FROM "+PropertiesClass.props.DB_NAME+".Carrier WHERE DataName='"+jComboBox2.getSelectedItem().toString()+"';";
            if(sqlConnector.checkQueryAppearence(query)) carrierID = Integer.parseInt(sqlConnector.getQueryFirstElement(query));         
        }
        
        sqlConnector.updateUser(jTextField1.getText(), new String(jPasswordField1.getPassword()), jTextField3.getText(), jTextField4.getText(), jTextField6.getText(), jTextField5.getText(), jTextArea1.getText(), MainPanel.userLogin, groupID,carrierID,selectedUser);
        editUserPanel.setVisible(false);
        UserPanel.userPanel.setVisible(true);
        UserPanel.userPanel.refreshItemList();
       }else JOptionPane.showMessageDialog(editUserPanel, "User login is empty or already exists! Chose new login or leave previous one.");
        
    }//GEN-LAST:event_editButtonActionPerformed
    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        if (evt.getValueIsAdjusting() == false) {
            int selectedIndex = jList1.getSelectedIndex();
            if (selectedIndex == -1) {
                //No selection, disable fire button.
                editButton.setEnabled(false);
            } else {
                if(jList1.getSelectedValue().toString().matches("Courier") ) {
                    jComboBox2.setVisible(true);
                    jComboBox2.setSelectedIndex(0);
                    carrierLabel.setVisible(true);
                }else 
                {
                    jComboBox2.setSelectedIndex(-1);
                    jComboBox2.setVisible(false);
                    carrierLabel.setVisible(false);
                }
                //Selection, enable the fire button.
                editButton.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jList1ValueChanged

