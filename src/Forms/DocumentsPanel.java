        jTextField2.setText(documentDetails[1]);
        jTextArea2.setText(documentDetails[2]);
        jTextField4.setText(documentDetails[3]);
        jTextField5.setText(documentDetails[4]);
        jTextField6.setText(documentDetails[5]);
        jTextField7.setText(documentDetails[6]);
        query = "SELECT ID FROM "+PropertiesClass.props.DB_NAME+".LinkedDoc WHERE FileName='"+jList1.getSelectedValue()+"' AND Transports_ID = '"+selectedEventID+"';";
