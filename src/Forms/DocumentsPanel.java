        query = "SELECT Label FROM "+PropertiesClass.props.DB_NAME+".Label WHERE LinkedDoc_ID = '"+docID+"';";
        jTextArea1.setText("");
        if(sqlConnector.checkQueryAppearence(query))
        {
          String[] labels = sqlConnector.getQueryFirstColumn(query);
        int i=0;
        while(i<labels.length){
            
            jTextArea1.setText(jTextArea1.getText()+labels[i]+"\n");
            i++;
        }  
        }
        
        
           
