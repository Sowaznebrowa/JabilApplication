    private void refreshItemList() {

        DefaultListModel listModel = new DefaultListModel();
        String query = "SELECT DataName FROM "+PropertiesClass.props.DB_NAME+".Group WHERE DataActive ='1' ORDER BY DataName ASC;";
        groupNames = sqlConnector.getQueryFirstColumn(query);

        for (int i = 0; i < groupNames.length; i++) {
            listModel.addElement(groupNames[i]);
        }
        jList1.setModel(listModel);
        
        DefaultComboBoxModel comboModel = new DefaultComboBoxModel();
        query = "SELECT DataName FROM "+PropertiesClass.props.DB_NAME+".Carrier WHERE DataActive ='1' ORDER BY DataName ASC;";
        carrierNames = sqlConnector.getQueryFirstColumn(query);

        for (int i = 0; i < carrierNames.length; i++) {
            comboModel.addElement(carrierNames[i]);
        }
        jComboBox2.setModel(comboModel);        
    }
    private void loadItemDetails(){
     String query = "SELECT * FROM "+PropertiesClass.props.DB_NAME+".User WHERE Login = '"+selectedUser+"';";
        String[] userDetails = sqlConnector.getQueryFirstRow(query,12);
        jTextField1.setText(userDetails[1]);
        jPasswordField1.setText(userDetails[2]);
        jTextField3.setText(userDetails[3]);
        jTextField4.setText(userDetails[4]);
        jTextField6.setText(userDetails[5]);
        jTextField5.setText(userDetails[6]);
        jTextArea1.setText(userDetails[7]);

        query = "SELECT DataName FROM "+PropertiesClass.props.DB_NAME+".Group WHERE ID = '"+userDetails[10]+"';";
        String groupName = sqlConnector.getQueryFirstElement(query);
        jList1.setSelectedValue(groupName, true);
        
        query = "SELECT DataName FROM "+PropertiesClass.props.DB_NAME+".Carrier WHERE ID = '"+userDetails[11]+"';";       
        if(sqlConnector.checkQueryAppearence(query)) jComboBox2.setSelectedItem(sqlConnector.getQueryFirstElement(query));
        else jComboBox2.setSelectedIndex(-1);
        
        
        
                   
    }
