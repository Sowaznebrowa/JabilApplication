    private void refreshItemList() {

        DefaultComboBoxModel listModel = new DefaultComboBoxModel();
        String query = "SELECT DataName FROM "+PropertiesClass.props.DB_NAME+".Group WHERE DataActive ='1' ORDER BY DataName ASC;";
        groupNames = sqlConnector.getQueryFirstColumn(query);

        for (int i = 0; i < groupNames.length; i++) {
            listModel.addElement(groupNames[i]);
        }
        jComboBox1.setModel(listModel);  
        
        listModel = new DefaultComboBoxModel();
        query = "SELECT DataName FROM "+PropertiesClass.props.DB_NAME+".Carrier WHERE DataActive ='1' ORDER BY DataName ASC;";
        carrierNames = sqlConnector.getQueryFirstColumn(query);

        //ver.1.02 New field added to describe Courier user group -- 29.11.2014
        for (int i = 0; i < carrierNames.length; i++) {
            listModel.addElement(carrierNames[i]);
        }
        jComboBox2.setModel(listModel);
    }
