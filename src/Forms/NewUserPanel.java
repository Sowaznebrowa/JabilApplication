    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String query = "SELECT * FROM "+PropertiesClass.props.DB_NAME+".User WHERE Login='"+jTextField1.getText()+"';";
        boolean check = sqlConnector.checkQueryAppearence(query);
        if(!check && !(jTextField1.getText().isEmpty())){
        query = "SELECT ID FROM "+PropertiesClass.props.DB_NAME+".Group WHERE DataName='"+jComboBox1.getSelectedItem().toString()+"';";
        int groupID = Integer.parseInt(sqlConnector.getQueryFirstElement(query));
        
        //ver.1.02 New field added to describe Courier user group -- 29.11.2014
        int carrierID=-1;
        if(jComboBox2.getSelectedIndex()!=-1){
        query = "SELECT ID FROM "+PropertiesClass.props.DB_NAME+".Carrier WHERE DataName='"+jComboBox2.getSelectedItem().toString()+"';";
        if(sqlConnector.checkQueryAppearence(query)) carrierID = Integer.parseInt(sqlConnector.getQueryFirstElement(query));        
        }
        
        sqlConnector.addUserToTable(jTextField1.getText(), new String(jPasswordField1.getPassword()), jTextField3.getText(), jTextField4.getText(), jTextField6.getText(), jTextField5.getText(), jTextArea1.getText(), MainPanel.userLogin, groupID, carrierID);
        newUserPanel.setVisible(false);
        UserPanel.userPanel.setVisible(true);
        UserPanel.userPanel.refreshItemList();
       }else JOptionPane.showMessageDialog(newUserPanel, "User login is empty or already exists! Chose new login or edit existing user.");
        
    }//GEN-LAST:event_addButtonActionPerformed
    
    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            int selectedIndex = jComboBox1.getSelectedIndex();
            if (selectedIndex == -1) {
                //No selection, disable fire button.
                addButton.setEnabled(false);
            } else {
                if(jComboBox1.getSelectedItem().toString().matches("Courier") ) {
                    jComboBox2.setVisible(true);
                    jComboBox2.setSelectedIndex(0);
                    carrierLabel.setVisible(true);
                }else 
                {
                    jComboBox2.setSelectedIndex(-1);
                    jComboBox2.setVisible(false);
                    carrierLabel.setVisible(false);
                }
                //Selection, enable the fire button.
                addButton.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void loadFromFileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadFromFileButtonActionPerformed
            JFileChooser chooser = new JFileChooser();
            int returnVal = chooser.showOpenDialog(null);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
            file = chooser.getSelectedFile(); // path to file
            //String fileName = file.getName();
            FileManager fm = new FileManager();
            fm.csvUserReader(file);
            JOptionPane.showMessageDialog(newUserPanel, "Data Loaded. Check \"*_report.csv\" file in loaded directory for more details.");
            UserPanel.userPanel.refreshItemList();
        }
    }//GEN-LAST:event_loadFromFileButtonActionPerformed

