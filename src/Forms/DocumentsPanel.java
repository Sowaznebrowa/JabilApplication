    private void downloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downloadButtonActionPerformed
        JFileChooser chooser = new JFileChooser();
        int index = jList1.getSelectedIndex();
        String fileName =fileNames[index];
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int returnVal = chooser.showSaveDialog(null);
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            Classes.FileManager fileManagerInstance = new Classes.FileManager();
            fileManagerInstance.fileRead(file,fileName);
            sqlConnector.addLogToTable("File Downloaded", MainPanel.userLogin, fileName, selectedEventID);
            JOptionPane.showMessageDialog(this, "File downloaded.");
            this.repaint();         
        }
    }//GEN-LAST:event_downloadButtonActionPerformed
